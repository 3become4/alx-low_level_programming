#include "3-calc.h"

/**
 * op_add -  add 2 integers.
 *
 * @a: int number
 * @b: int number
 *
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub -  subtract 2 integers.
 *
 * @a: int number
 * @b: int number
 *
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul -  multiply 2 integers.
 *
 * @a: int number
 * @b: int number
 *
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -  devide 2 integers.
 *
 * @a: int number
 * @b: int number
 *
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -  devide 2 integers.
 *
 * @a: int number
 * @b: int number
 *
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}

